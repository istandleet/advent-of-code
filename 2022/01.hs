    input <- readFile "01.txt" :: IO String
    let nums = map (map read . filter (not . null)) $ Data.List.groupBy (\ a b -> not (null b)) $ lines input :: [[Int]]
    print $ maximum $ map sum nums
    print $ sum $ take 3 $ reverse $ Data.List.sort $ map sum nums

